// 确认批量删除
async function mw_confirmBatchDelete() {
  mw_hideDeleteConfirm();

  const checkboxes = document.querySelectorAll('.doc-checkbox:checked');
  let docs = mw_loadDocs();
  const selectedIds = Array.from(checkboxes).map(cb => cb.dataset.docId);

  // 标记选中文档为已删除
  let deletedCount = 0;
  selectedIds.forEach(id => {
    const idx = docs.findIndex(d => d.id === id);
    if (idx >= 0) {
      docs[idx] = {
        id: docs[idx].id,
        name: docs[idx].name,
        deletedAt: Date.now(),
        updatedAt: Date.now()
      };
      deletedCount++;
    }
  });

  mw_saveDocs(docs);

  // 删除关联的图片
  if (window.imageStorage) {
    let totalDeletedImages = 0;
    for (const docId of selectedIds) {
      try {
        const deletedImagesCount = await window.imageStorage.deleteImagesByDocumentId(docId);
        totalDeletedImages += deletedImagesCount;
        console.log(`删除了文档 ${docId} 的 ${deletedImagesCount} 张图片`);
      } catch (error) {
        console.error(`删除文档 ${docId} 关联图片失败:`, error);
      }
    }

    // 同时删除所有未关联到文档的图片（documentId为null的图片）
    try {
      const unlinkedImages = await window.imageStorage.getAllImages(null);
      const unlinkedImageIds = unlinkedImages
        .filter(img => img.documentId === null)
        .map(img => img.id);

      for (const imageId of unlinkedImageIds) {
        try {
          await window.imageStorage.deleteImage(imageId);
          totalDeletedImages++;
        } catch (error) {
          console.error(`删除未关联图片 ${imageId} 失败:`, error);
        }
      }

      if (unlinkedImageIds.length > 0) {
        console.log(`删除了 ${unlinkedImageIds.length} 张未关联到文档的图片`);
      }
    } catch (error) {
      console.error(`删除未关联图片失败:`, error);
    }

    if (totalDeletedImages > 0) {
      console.log(`批量删除文档时总共删除了 ${totalDeletedImages} 张图片`);
    }
  }

  // 如果删除的文档中包含当前活动文档
  const activeId = mw_getActive();
  if (selectedIds.includes(activeId)) {
    // 清空当前活动文档
    mw_setActive('');

    // 找到第一个未删除的文档
    const activeDoc = docs.find(d => !d.deletedAt);
    if (activeDoc) {
      mw_setActive(activeDoc.id);
      // 通知各个面板加载第一个未删除文档
      mw_notifyEditorLoad(activeDoc);
      mw_notifyPreviewLoad(activeDoc);
      mw_notifyMindmapLoad(activeDoc);
    } else {
      // 没有剩余未删除文档，不再自动生成空文档
      // 清空活动文档，让用户手动创建新文档
      console.log('没有剩余文档，等待用户手动创建');
    }
  }

  // 退出批量模式
  mw_exitBatchMode();
  mw_renderList();
  try { showSuccess && showSuccess(`已删除 ${deletedCount} 个文档，将在所有设备同步`); } catch (_) { }
}

