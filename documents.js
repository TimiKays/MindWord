// 确认删除单个文档
async function mw_confirmDeleteDoc() {
  const dialog = document.getElementById('delete-confirm-dialog');
  const docId = dialog.dataset.docId;

  if (!docId) return;

  mw_hideDeleteConfirm();

  let docs = mw_loadDocs();
  const idx = docs.findIndex(d => d.id === docId);
  if (idx >= 0) {
    docs[idx] = {
      id: docs[idx].id,
      name: docs[idx].name,
      deletedAt: Date.now(),
      updatedAt: Date.now()
    };
    mw_saveDocs(docs);

    // 删除关联的图片
    if (window.imageStorage) {
      try {
        const deletedImagesCount = await window.imageStorage.deleteImagesByDocumentId(docId);
        console.log(`删除了文档 ${docId} 的 ${deletedImagesCount} 张图片`);

        // 同时检查并删除未关联到文档的图片（documentId为null的图片）
        const unlinkedImages = await window.imageStorage.getAllImages(null);
        const unlinkedImageIds = unlinkedImages
          .filter(img => img.documentId === null)
          .map(img => img.id);

        let unlinkedDeletedCount = 0;
        for (const imageId of unlinkedImageIds) {
          try {
            await window.imageStorage.deleteImage(imageId);
            unlinkedDeletedCount++;
          } catch (error) {
            console.error(`删除未关联图片 ${imageId} 失败:`, error);
          }
        }

        if (unlinkedDeletedCount > 0) {
          console.log(`删除了 ${unlinkedDeletedCount} 张未关联到文档的图片`);
        }
      } catch (error) {
        console.error(`删除文档 ${docId} 关联图片失败:`, error);
      }
    }

    // 如果删除的是当前活动文档
    const activeId = mw_getActive();
    if (docId === activeId) {
      // 清空当前活动文档
      mw_setActive('');

      // 找到第一个未删除的文档
      const activeDoc = docs.find(d => !d.deletedAt);
      if (activeDoc) {
        mw_setActive(activeDoc.id);
        // 通知各个面板加载第一个未删除文档
        mw_notifyEditorLoad(activeDoc);
        mw_notifyPreviewLoad(activeDoc);
        mw_notifyMindmapLoad(activeDoc);
      } else {
        // 没有剩余未删除文档，不再自动生成未命名文档，等待用户手动创建
        console.log('没有剩余文档，等待用户手动创建');
        // 清空当前活动文档
        mw_setActive(null);
        mw_renderList();
        // 通知各个面板清空内容
        mw_notifyEditorLoad({ id: null, name: '', md: '', images: [] });
        mw_notifyPreviewLoad({ md: '', images: [] });
        mw_notifyMindmapLoad({ md: '', images: [] });
      }
    }

    mw_renderList();
    try { showSuccess && showSuccess('文档已删除，将在所有设备同步'); } catch (_) { }
  }
}

