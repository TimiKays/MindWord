// 判断文档是否是自动生成的空文档（没有真实子节点）
function isAutoGeneratedEmptyDoc(doc) {
  if (!doc) return false;

  // 检查名称是否为默认的"未命名文档"
  const name = doc.name || '';
  if (name !== '未命名文档') return false;

  // 检查内容是否为默认的"# 未命名文档\n"
  const md = doc.md || '';
  if (md.trim() !== '# 未命名文档') return false;

  // 检查是否有图片（空文档不应该有图片）
  if (doc.images && doc.images.length > 0) return false;

  return true;
}

function mw_extractTitleFromMd(md) {
  try {
    const lines = String(md || '').split(/\r?\n/);
    const first = (lines.find(l => l.trim().length > 0) || '').trim();
    if (!first) return '未命名文档';
    const cleaned = first
      .replace(/^#{1,6}\s*/, '')
      .replace(/^[-*+]\s+/, '')
      .replace(/^\d+\.\s+/, '')
      .trim();
    return cleaned || '未命名文档';
  } catch (_) { return '未命名文档'; }
}
function mw_guessMime(name) {
  const lower = (name || '').toLowerCase();
  if (lower.endsWith('.png')) return 'image/png';
  if (lower.endsWith('.jpg') || lower.endsWith('.jpeg')) return 'image/jpeg';
  if (lower.endsWith('.gif')) return 'image/gif';
  if (lower.endsWith('.webp')) return 'image/webp';
  return 'application/octet-stream';
}
