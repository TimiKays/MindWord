    // 计算文件大小（包括文档内容和图片）
    function calculateDataSize(docs) {
        let totalSize = 0;
        let imageCount = 0;

        for (const doc of (docs || [])) {
            if (!doc) continue;

            // 计算文档文本内容大小
            if (doc.md) {
                totalSize += new Blob([doc.md]).size;
            }

            // 计算标题大小
            if (doc.name) {
                totalSize += new Blob([doc.name]).size;
            }

            // 计算备注大小
            if (doc.note) {
                totalSize += new Blob([doc.note]).size;
            }

            // 计算文档中图片的大小
            if (doc.images && Array.isArray(doc.images)) {
                for (const img of doc.images) {
                    const imgData = img.dataUrl || img.data;
                    if (imgData) {
                        const base64Size = imgData.length * 0.75;
                        totalSize += base64Size;
                        imageCount++;
                    }
                }
            }

            // 检查 markdown 中的 base64 图片
            if (doc.md) {
                const imgMatches = doc.md.match(/data:image\/[^;]+;base64,[^"']+/g) || [];
                for (const imgData of imgMatches) {
                    const base64Data = imgData.split(',')[1];
                    if (base64Data) {
                        totalSize += base64Data.length * 0.75;
                        imageCount++;
                    }
                }
            }
        }

        return { totalSize, imageCount };
    }

    // 格式化相对时间
    function formatRelativeTime(timestamp) {
        if (!timestamp) return '';

        const date = new Date(timestamp);
        const now = new Date();
        const diffMs = now - date;
        const diffMins = Math.floor(diffMs / 60000);
        const diffHours = Math.floor(diffMs / 3600000);
        const diffDays = Math.floor(diffMs / 86400000);

        if (diffMins < 1) {
            return '刚刚';
        } else if (diffMins < 60) {
            return `${diffMins}分钟前`;
        } else if (diffHours < 24) {
            return `${diffHours}小时前`;
        } else if (diffDays < 7) {
            return `${diffDays}天前`;
        } else {
            return date.toLocaleDateString('zh-CN', { month: 'short', day: 'numeric' });
        }
    }

    // 格式化文件大小
    function formatFileSize(bytes) {
        if (bytes < 1024) {
            return bytes + 'B';
        } else if (bytes < 1024 * 1024) {
            return (bytes / 1024).toFixed(1) + 'KB';
        } else {
            return (bytes / 1024 / 1024).toFixed(1) + 'MB';
        }
    }

