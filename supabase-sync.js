    async function extractImagesToIndexedDB(docs) {
        if (!window.imageStorage) {
            console.warn(MODULE_NAME, '图片存储管理器未初始化，跳过图片提取');
            return { saved: 0, skipped: 0, errors: 0 };
        }

        let savedCount = 0;
        let skippedCount = 0;
        let errorCount = 0;

        for (const doc of (docs || [])) {
            if (!doc || !doc.id) continue;
            if (!doc.images || !Array.isArray(doc.images)) continue;

            for (const img of doc.images) {
                if (!img || !img.id) continue;

                const dataUrl = img.dataUrl || img.data;
                if (!dataUrl) {
                    skippedCount++;
                    continue;
                }

                try {
                    const existingImage = await window.imageStorage.getImage(img.id);
                    if (existingImage && existingImage.blob) {
                        skippedCount++;
                        continue;
                    }

                    const blob = await window.imageStorage.dataUrlToBlob(dataUrl);
                    await window.imageStorage.saveImage(img.id, blob, {
                        name: img.name || img.id,
                        documentId: doc.id
                    });
                    savedCount++;
                    console.log(MODULE_NAME, `提取图片到IndexedDB: ${img.id} (文档: ${doc.id})`);
                } catch (error) {
                    errorCount++;
                    console.error(MODULE_NAME, `提取图片失败 ${img.id}:`, error);
                }
            }
        }

        console.log(MODULE_NAME, `图片提取完成: 保存${savedCount}张, 跳过${skippedCount}张, 失败${errorCount}张`);
        return { saved: savedCount, skipped: skippedCount, errors: errorCount };
    }

    async function embedImagesFromIndexedDB(docs) {
        if (!window.imageStorage) {
            console.warn(MODULE_NAME, '图片存储管理器未初始化，跳过图片嵌入');
            return docs;
        }

        const updatedDocs = [];

        for (const doc of (docs || [])) {
            if (!doc || !doc.id) {
                updatedDocs.push(doc);
                continue;
            }

            const imageIds = extractImageIdsFromDoc(doc);
            if (imageIds.length === 0) {
                updatedDocs.push(doc);
                continue;
            }

            const imagesArray = Array.isArray(doc.images) ? [...doc.images] : [];
            const existingIds = new Set(imagesArray.map(img => img && img.id));

            for (const imageId of imageIds) {
                if (existingIds.has(imageId)) continue;

                try {
                    const imageData = await window.imageStorage.getImage(imageId);
                    if (!imageData || !imageData.blob) continue;

                    const dataUrl = await window.imageStorage.blobToDataUrl(imageData.blob);
                    imagesArray.push({
                        id: imageId,
                        name: imageData.name || imageId,
                        mime: imageData.type || 'image/png',
                        dataUrl: dataUrl
                    });
                } catch (error) {
                    console.error(MODULE_NAME, `嵌入图片失败 ${imageId}:`, error);
                }
            }

            for (const img of imagesArray) {
                if (!img || !img.id) continue;
                if (img.dataUrl || img.data) continue;

                try {
                    const imageData = await window.imageStorage.getImage(img.id);
                    if (!imageData || !imageData.blob) continue;

                    const dataUrl = await window.imageStorage.blobToDataUrl(imageData.blob);
                    img.dataUrl = dataUrl;
                } catch (error) {
                    console.error(MODULE_NAME, `补全图片数据失败 ${img.id}:`, error);
                }
            }

            updatedDocs.push({ ...doc, images: imagesArray });
        }

        return updatedDocs;
    }

    function extractImageIdsFromDoc(doc) {
        const imageIds = [];
        const content = (doc.md || '') + (doc.note || '');
        const imageRegex = /!\[[^\]]*\]\(([^)]+)\)/g;
        let match;
        while ((match = imageRegex.exec(content)) !== null) {
            const imageId = match[1];
            if (imageId && !imageId.startsWith('http') && !imageId.startsWith('data:')) {
                imageIds.push(imageId);
            }
        }
        return [...new Set(imageIds)];
    }

    async function setLocalData(data) {
        return new Promise((resolve) => {
            try {
                // 保存文档数据
                if (data.docs && Array.isArray(data.docs)) {
                    if (typeof window.mw_saveDocs === 'function') {
                        window.mw_saveDocs(data.docs);
                    } else if (typeof mw_saveDocs === 'function') {
                        mw_saveDocs(data.docs);
                    } else {
                        localStorage.setItem('mw_documents', JSON.stringify(data.docs));
                    }

                    // 触发文档列表刷新
                    if (typeof window.mw_renderList === 'function') {
                        window.mw_renderList();
                    } else if (typeof mw_renderList === 'function') {
                        mw_renderList();
                    }
                }

                // 保存 AI 配置 - 使用与LeanCloud一致的键名
                if (data.aiConfig) {
                    localStorage.setItem('allAIPlatformConfigs', JSON.stringify(data.aiConfig));
                }

                // 保存提示词模板 - 使用与LeanCloud一致的键名
                if (data.promptTemplates) {
                    localStorage.setItem('promptTemplates', JSON.stringify(data.promptTemplates));
                }

                // 保存我的提示词模板 - 使用与LeanCloud一致的键名
                if (data.myPromptTemplates) {
                    localStorage.setItem('myPromptTemplates', JSON.stringify(data.myPromptTemplates));
                }

                // 零时间戳和空哈希也是同步预览的明确结果，不能遗留上一轮状态。
                const timestampMappings = [
                    ['docUpdatedAt', 'mw_doc_updated_at'],
                    ['aiConfigLastModified', 'allAIPlatformConfigs_last_modified'],
                    ['promptTemplatesLastModified', 'promptTemplates_last_modified'],
                    ['myPromptTemplatesLastModified', 'myPromptTemplates_last_modified']
                ];
                timestampMappings.forEach(function ([field, storageKey]) {
                    if (Object.prototype.hasOwnProperty.call(data, field)) {
                        localStorage.setItem(storageKey, String(Number(data[field] || 0)));
                    }
                });

                const hashMappings = [
                    ['aiConfigHash', 'allAIPlatformConfigs_hash'],
                    ['promptTemplatesHash', 'promptTemplates_hash'],
                    ['myPromptTemplatesHash', 'myPromptTemplates_hash']
                ];
                hashMappings.forEach(function ([field, storageKey]) {
                    if (!Object.prototype.hasOwnProperty.call(data, field)) return;
                    if (data[field]) localStorage.setItem(storageKey, data[field]);
                    else localStorage.removeItem(storageKey);
                });

                console.log(MODULE_NAME, '本地数据已更新');
                resolve();
            } catch (error) {
                console.error(MODULE_NAME, '保存本地数据失败:', error);
                resolve();
            }
        });
    }

